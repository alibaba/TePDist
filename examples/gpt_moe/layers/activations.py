import numpy as np
import tensorflow.compat.v1 as tf

from model import tffloat


def get_activation_fn(activation):
    if activation == "gelu":
        return gelu
    if activation == "gelu_new":
        return gelu_new
    return tf.keras.activations.get(activation)


def gelu(x):
    """ Gaussian Error Linear Unit.
    Original Implementation of the gelu activation function in Google Bert repo when initially created.
        For information: OpenAI GPT's gelu is slightly different (and gives slightly different results):
        0.5 * x * (1 + torch.tanh(math.sqrt(2 / math.pi) * (x + 0.044715 * torch.pow(x, 3))))
        Also see https://arxiv.org/abs/1606.08415
    """
    cdf = tf.constant(0.5, dtype=tffloat) * (
                tf.constant(1.0, dtype=tffloat) + tf.math.erf(x / tf.math.sqrt(tf.constant(2.0, dtype=tffloat))))
    return x * cdf


def gelu_new(x):
    """Gaussian Error Linear Unit.
    This is a smoother version of the RELU.
    Original paper: https://arxiv.org/abs/1606.08415
    Args:
        x: float Tensor to perform activation.
    Returns:
        `x` with the GELU activation applied.
    """
    cdf = 0.5 * (1.0 + tf.tanh((np.sqrt(2 / np.pi) * (x + 0.044715 * tf.pow(x, 3)))))
    return x * cdf


def swish(x):
    return x * tf.sigmoid(x)


def relu(x):
    return tf.nn.relu(x)
